<template>
  <div class="login-container">
    <el-form
      ref="loginForm"
      :model="loginForm"
      :rules="loginRules"
      class="login-form"
      auto-complete="on"
      label-position="left"
    >
      <div class="title-container">
        <h3 class="title">Login Form</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-form-item prop="captcha">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="captcha"
          v-model="loginForm.captcha"
          placeholder="Captcha"
          name="captcha"
          type="text"
          tabindex="3"
          auto-complete="on"
        />
        <div style="position: absolute;left: 78.5%;top: 1px;">
          <img :src="'data:image/png;base64,'+captcha" alt @click="getCaptcha" />
        </div>
      </el-form-item>

      <el-button
        :loading="loading"
        type="primary"
        style="width:100%;margin-bottom:30px;"
        @click.native.prevent="handleLogin"
      >Login</el-button>
    </el-form>
  </div>
</template>

<script>
export default {
  name: "Login",
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!value.trim()) {
        callback(new Error("请输入用户名"));
      } else if (value.trim().length < 5) {
        callback(new Error("用户名最少5个字符"));
      } else {
        callback();
      }
    };
    const validatePassword = (rule, value, callback) => {
      if (!value.trim()) {
        callback(new Error("请输入密码"));
      } else if (value.trim().length < 6) {
        callback(new Error("密码最少5个字符"));
      } else {
        callback();
      }
    };
    return {
      captcha: "",
      isClick: true,
      isNum: 0,
      loginForm: {
        username: "",
        password: "",
        captcha:"",
      },
      loginRules: {
        username: [
          { required: true, trigger: "blur", validator: validateUsername }
        ],
        password: [
          { required: true, trigger: "blur", validator: validatePassword }
        ],
        captcha:[
          { required: true, message: "请输入验证码", trigger: "blur" },
          { min: 4,  trigger: "blur" ,message:"验证码4个数字或字符组成"}
        ]
      },
      loading: false,
      passwordType: "password",
      redirect: undefined
    };
  },
  watch: {
    $route: {
      handler: function(route) {
        // console.log("route.query",route.query)
        this.redirect = route.query && route.query.redirect;
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === "password") {
        this.passwordType = "";
      } else {
        this.passwordType = "password";
      }
      this.$nextTick(() => {
        this.$refs.password.focus();
      });
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true;
          this.$store
            .dispatch("user/login", this.loginForm)
            .then(() => {
              this.$router.push({ path: "/" });
              this.loading = false;
            })
            .catch(() => {
              this.loading = false;
            });
        } else {
          return false;
        }
      });
    },
    //获取验证码
    getCaptcha() {
      if (this.isNum === 0) {
        this.isNum = 1;
        this.$store.dispatch("user/getLogin").then(response => {
          this.captcha = response.captcha;
          this.isClick = true;
        });
      } else {
        if (this.isClick) {
          this.isClick = false;
          setTimeout(() => {
            this.$store.dispatch("user/getLogin").then(response => {
              this.captcha = response.captcha;
              this.isClick = true;
            });
          }, 2000);
        }
      }
    }
  },
  created() {
    this.getCaptcha();
  }
};
</script>

